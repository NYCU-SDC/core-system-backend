#!/usr/bin/env bash

# This script merges all schema.sql files found in the internal directory and subdirectories into a single SQL file.
# It is used to create a full schema file for sqlc.

set -e

# Default values
OUTPUT_FILE="./internal/database/full_schema.sql"
SQLC_YAML="./sqlc.yaml"

# Print help message
print_help() {
    echo "Usage: $0 [options]"
    echo
    echo "Options:"
    echo "  --output-file PATH     Path to the merged schema output file (default: ./internal/database/full_schema.sql)"
    echo "  --sqlc-yaml PATH       Path to the generated sqlc.yaml file (default: ./sqlc.yaml)"
    echo "  --help                 Show this help message and exit"
}

# Parse arguments
while [[ $# -gt 0 ]]; do
    case "$1" in
        --output-file)
            OUTPUT_FILE="$2"
            shift 2
            ;;
        --sqlc-yaml)
            SQLC_YAML="$2"
            shift 2
            ;;
        --help)
            print_help
            exit 0
            ;;
        *)
            echo "Unknown option: $1"
            print_help
            exit 1
            ;;
    esac
done

SCHEMA_FILES=()
QUERY_FILES=()

# Discover files in stable path order so generated output is reproducible across machines.
mapfile -t SCHEMA_FILES < <(find ./internal -type f -name "schema.sql" | LC_ALL=C sort)
mapfile -t QUERY_FILES < <(find ./internal -type f -name "queries.sql" | LC_ALL=C sort)

# Clear and initialize the schema file
echo -e "-- Code generated by schema merge script. DO NOT EDIT.\n" > "$OUTPUT_FILE"

# Merge schema files
for SCHEMA in "${SCHEMA_FILES[@]}"; do
    cat "$SCHEMA" >> "$OUTPUT_FILE"
done

# Start generating sqlc.yaml
cat > "$SQLC_YAML" << 'EOF'
# Code generated by schema merge script. DO NOT EDIT.
version: "2"
sql:
EOF

# Generate config for each queries.sql package in the same stable order.
for QUERY_FILE in "${QUERY_FILES[@]}"; do
    PACKAGE_DIR=$(dirname "$QUERY_FILE")
    PACKAGE_NAME=$(basename "$PACKAGE_DIR")

    if [ "$PACKAGE_NAME" = "user" ]; then
        cat >> "$SQLC_YAML" << EOF
  - engine: "postgresql"
    queries: "$QUERY_FILE"
    schema: "$OUTPUT_FILE"
    gen:
      go:
        package: "$PACKAGE_NAME"
        out: "$PACKAGE_DIR"
        sql_package: "pgx/v5"
        rename:
          users_with_email: UserWithEmails
        overrides:
          - db_type: "uuid"
            go_type:
              import: "github.com/google/uuid"
              type: "UUID"
EOF
    else
        cat >> "$SQLC_YAML" << EOF
  - engine: "postgresql"
    queries: "$QUERY_FILE"
    schema: "$OUTPUT_FILE"
    gen:
      go:
        package: "$PACKAGE_NAME"
        out: "$PACKAGE_DIR"
        sql_package: "pgx/v5"
        overrides:
          - db_type: "uuid"
            go_type:
              import: "github.com/google/uuid"
              type: "UUID"
EOF
    fi
done
